package za.co.vodacom.news.data;

/**
 * Holds data for displaying a tweet
 * 
 * @author tobykurien
 * 
 */
public class Tweet {
	private String tweet;
	private String date;

	public String getTweet() {
		return tweet;
	}

	public void setTweet(String tweet) {
		this.tweet = tweet;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
}
